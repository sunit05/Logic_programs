package logic;

public class Num_3to0 {
	public static void main(String[] args) {
		int n=3;
		for(int i=1;i<=n;i++){
			int temp=n;
//			for(int m=)
			for(int j=1;j<=temp;j++) {
				System.out.print(temp);
			}temp--;
			for(int k=1;k<=n;k++) {
				System.out.print(temp);
			}temp--;
			for(int p=1;p<=n;p++) {
				System.out.print(temp);
			}
		System.out.println();
		temp--;
		}
	}
}
